  <div class="line container">
    <h2>Definitions</h2>
    <p><strong>Market pulp:</strong> Market pulp is pulp that is sold in open competition with that of other producers. All pulp exported from the producing country is considered to be market pulp.</p>
    <p><strong>Consumption:</strong> Market pulp tonnages consumed at the mills over the month concerned.</p>
    <p><strong>Inventories:</strong> Consumer market pulp which is already owned by the consumers, i.e. pulp that has already been delivered to the paper mill, or landed at harbours, in transit (barge, train, truck) and at storehouses where it is available to the paper mill. This excludes supplier-owned consignment stocks and those on the high seas.</p>
    <p><strong>Inventories in days of consumption:</strong> inventories M x 360 / consumption of the past 12 months</p>
    <p><strong>M:</strong> the month concerned</p>
